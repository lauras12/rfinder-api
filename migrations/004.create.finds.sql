DROP TABLE IF EXISTS findText;
CREATE TABLE findText (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    description TEXT
)


DROP TABLE IF EXISTS findChecked;
CREATE TABLE findChecked (
     id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
     userId INTEGER REFERENCES user(id) ON DELETE SET NOT NULL,
     placeId INTEGER REFERENCES place(id) ON DELETE CASCADE NULL,
     reviewId INTEGER REFERENCES review(id) ON DELETE CASCADE NULL,
     find INTEGER REFERENCES findText(description) ON DELETE SET NULL,
)
